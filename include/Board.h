#ifndef BOARD_H
// ^^^^^^^^^^^
// Это "include guard" — предотвращает двойное включение заголовка.
// Если компилятор уже видел BOARD_H, то всё содержимое файла будет проигнорировано.
// Это важно, когда один и тот же заголовок подключается из нескольких файлов.

#define BOARD_H
// ^^^^^^^^^^^
// Устанавливаем макрос BOARD_H — теперь, если снова попытаемся включить этот файл,
// #ifndef сработает и содержимое не будет прочитано снова.

#include <vector>
// ^^^^^^^^^^^^^^
// Подключаем стандартную библиотеку vector — динамический массив.
// std::vector — аналог массива, но с автоматическим управлением памятью.

#include <string>
// ^^^^^^^^^^^^^
// Подключаем библиотеку string — для работы с текстовыми строками.
// std::string — удобная альтернатива char*, автоматически управляет памятью.

class Board {
// ^^^^^^^^^^
// Объявление класса Board — это "шаблон" или "чертёж" для создания объектов.
// Всё, что внутри класса — относится к одному "экземпляру" доски (Board).

public:
// ^^^^^^
// Секция public — всё, что здесь, доступно извне класса.
// Например, можно вызвать board.print() или board.setCell().

    Board();  // конструктор — вызывается при создании объекта Board
    // ^^^^^^
    // Это специальная функция, которая инициализирует объект.
    // В нашем случае — заполняет поле символами '~'.

    void print() const;  // вывести поле в консоль
    // ^^^^^^^^^^^^^^^^
    // const — означает, что функция НЕ меняет состояние объекта.
    // Это важно: мы только читаем поле, не изменяя его.

    void setCell(int row, int col, char value);  // установить значение клетки
    // ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
    // Принимает координаты (row, col) и символ (value), чтобы установить в клетку.

    char getCell(int row, int col) const;  // получить значение клетки
    // ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
    // Возвращает символ из клетки (row, col).
    // Также помечена как const — не изменяет состояние.

private:
// ^^^^^^^
// Секция private — доступна только внутри класса.
// Внешний код не может напрямую обращаться к grid.
// Это защищает внутренние данные от случайного изменения.

    static constexpr int SIZE = 10;  // размер поля 10x10
    // ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
    // static — относится ко всему классу, а не к конкретному объекту.
    // constexpr — значение вычисляется на этапе компиляции.
    // Это константа, которую нельзя изменить.

    std::vector<std::vector<char>> grid;  // двумерная сетка
    // ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
    // Это двумерный вектор — массив массивов.
    // grid[row][col] — доступ к клетке.
    // Внешний вектор — строки, внутренний — столбцы.
    // char — символ в клетке ('~', 'S', 'X', и т.д.)
};

#endif // BOARD_H
// ^^^^^^^^^^^^^^
// Конец include guard — завершает защиту от повторного включения.
