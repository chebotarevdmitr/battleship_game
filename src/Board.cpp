Board::Board() {
// ^^^^^^^^^^^^
// Это определение (реализация) конструктора.
// Board:: — указывает, что функция принадлежит классу Board.
    // Инициализируем поле символом '~' — вода
    grid.resize(SIZE, std::vector<char>(SIZE, '~'));
    // ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
    // resize — изменяет размер вектора.
    // SIZE — 10 строк.
    // std::vector<char>(SIZE, '~') — строка из 10 символов '~'.
    // grid.resize — делает grid массивом 10x10, заполненным '~'.
}

void Board::print() const {
// ^^^^^^^^^^^^^^^^^^^^^
// Реализация функции print.
// const — функция не изменяет объект.
    std::cout << "  ";
    // ^^^^^^^^^^^^^^^^
    // Выводим отступ перед номерами столбцов.

    for (int i = 0; i < SIZE; ++i) {
    // ^^^^^^^^^^^^^^^^^^^^^^^^^^^
    // Цикл по столбцам — выводим номера столбцов (0-9).
        std::cout << std::setw(2) << i << " ";
        // ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
        // setw(2) — выделяет 2 символа под вывод (чтобы было ровно).
        // i — номер столбца.
    }
    std::cout << "\n";
    // ^^^^^^^^^^^^^^^
    // Переход на новую строку.

    for (int row = 0; row < SIZE; ++row) {
    // ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
    // Цикл по строкам — выводим содержимое поля.
        std::cout << std::setw(2) << row << " ";
        // ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
        // Выводим номер строки (0-9) и отступ.

        for (int col = 0; col < SIZE; ++col) {
        // ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
        // Цикл по столбцам — выводим содержимое каждой клетки.
            std::cout << std::setw(2) << grid[row][col] << " ";
            // ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
            // Выводим символ из клетки grid[row][col].
        }
        std::cout << "\n";
        // ^^^^^^^^^^^^^^^
        // Переход на новую строку после вывода строки поля.
    }
}

void Board::setCell(int row, int col, char value) {
// ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
// Реализация функции setCell.
    if (row >= 0 && row < SIZE && col >= 0 && col < SIZE) {
    // ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
    // Проверяем, что координаты находятся в пределах поля.
        grid[row][col] = value;
        // ^^^^^^^^^^^^^^^^^^^^
        // Устанавливаем значение в клетке.
    }
}

char Board::getCell(int row, int col) const {
// ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
// Реализация функции getCell.
    if (row >= 0 && row < SIZE && col >= 0 && col < SIZE) {
    // ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
    // Проверяем, что координаты находятся в пределах поля.
        return grid[row][col];
        // ^^^^^^^^^^^^^^^^^^^^
        // Возвращаем символ из клетки.
    }
    return ' '; // вне поля
    // ^^^^^^^^^^^^^^^^^^^^
    // Если координаты за пределами — возвращаем пробел.
}