#include "../include/Board.h"
// ^^^^^^^^^^^^^^^^^^^^^^^^^
// Подключаем заголовок Board, чтобы использовать класс Board.

int main() {
// ^^^^^^^^
// Главная функция программы — точка входа.
// int — возвращаемое значение (обычно 0 = успех).

    Board board;
    // ^^^^^^^^^
    // Создаём объект класса Board.
    // Вызывается конструктор Board() — поле заполняется '~'.

    board.setCell(2, 3, 'S');  // поставим корабль
    // ^^^^^^^^^^^^^^^^^^^^^^^
    // Вызываем метод setCell — устанавливаем символ 'S' в клетку [2][3].

    board.setCell(5, 7, 'X');  // попадание
    // ^^^^^^^^^^^^^^^^^^^^^^^
    // Устанавливаем символ 'X' в клетку [5][7].

    board.print();
    // ^^^^^^^^^^^
    // Выводим поле на экран.

    return 0;
    // ^^^^^^^^
    // Возвращаем 0 — признак успешного завершения программы.
}